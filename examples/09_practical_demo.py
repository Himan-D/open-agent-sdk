@tool(name="analyze_news", description="Analyze news articles and extract key points", category="research")
async def analyze_news(articles: str, topic: str) -> str:
    """Analyze a list of news articles and extract key insights."""
    lines = [l.strip() for l in articles.strip().split('\n') if l.strip()]
    
    insights = []
    insights.append(f"Topic: {topic}")
    insights.append(f"Articles found: {len(lines)}")
    insights.append(f"Analysis date: {datetime.now().strftime('%Y-%m-%d %H:%M')}")
    insights.append("")
    insights.append("Key Points:")
    
    for i, line in enumerate(lines[:5], 1):
        if ': ' in line:
            title = line.split(': ', 1)[0].replace('- ', '')
            insights.append(f"  {i}. {title}")
    
    insights.append("")
    insights.append("Summary:")
    insights.append(f"  Found {len(lines)} articles related to '{topic}'.")
    insights.append("  Top sources include major tech and news outlets.")
    insights.append("  Coverage appears active with recent developments.")
    
    return "\n".join(insights)


@tool(name="sentiment_headlines", description="Analyze sentiment of news headlines", category="research")
async def sentiment_headlines(headlines: str) -> str:
    """Analyze sentiment of news headlines."""
    positive_words = ["breakthrough", "growth", "success", "launch", "innovation", "leader", "award", "win", "record", "surge"]
    negative_words = ["crash", "fail", "loss", "lawsuit", "scandal", "recall", "hack", "breach", "decline", "warning"]
    
    lines = [l.strip() for l in headlines.strip().split('\n') if l.strip()]
    pos, neg, neutral = 0, 0, 0
    
    for line in lines:
        lower = line.lower()
        p = sum(1 for w in positive_words if w in lower)
        n = sum(1 for w in negative_words if w in lower)
        if p > n:
            pos += 1
        elif n > p:
            neg += 1
        else:
            neutral += 1
    
    total = len(lines) or 1
    return f"Sentiment: {pos/total*100:.0f}% positive, {neutral/total*100:.0f}% neutral, {neg/total*100:.0f}% negative"


@tool(name="generate_report", description="Generate a formatted news report", category="reporting")
async def generate_report(topic: str, search_results: str, analysis: str, sentiment: str) -> str:
    """Generate a formatted markdown report."""
    report = f"""# News Research Report: {topic}

**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}

## Search Results
{search_results}

## Key Analysis
{analysis}

## Sentiment Analysis
{sentiment}

## Top Stories Summary
Based on the research, here are the most relevant developments:

"""
    
    lines = [l.strip() for l in search_results.strip().split('\n') if l.strip()]
    for i, line in enumerate(lines[:5], 1):
        if ': ' in line:
            title = line.split(': ', 1)[0].replace('- ', '')
            report += f"{i}. **{title}** - {line.split(': ', 1)[1][:60]}...\n"
    
    report += f"""
## Recommendations
- Monitor key players in the {topic} space for updates
- Set up alerts for breaking developments
- Review competitive landscape periodically

---
*Report generated by SmithAI Agent Crew*
"""
    return report


